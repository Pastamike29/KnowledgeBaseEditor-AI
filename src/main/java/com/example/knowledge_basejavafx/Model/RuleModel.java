package com.example.knowledge_basejavafx.Model;


// RuleModel to set what variable/parameter have to get and store
public class RuleModel {

    private int id;
    private String fact;
    private String result;



    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFact() {
        return fact;
    }

    public void setFact(String fact) {
        this.fact = fact;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }
}
